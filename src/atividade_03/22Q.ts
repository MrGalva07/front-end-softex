//Receba a base e a altura de um retângulo digitados pelo usuário. Em seguida, calcule a sua área e
//exiba o resultado.
const base = Number(prompt('Informe a base do retângulo: '));
const altura = Number(prompt('Informe a altura do retângulo: '));
const area = base * altura;
alert("A área do retângulo é: " + area);