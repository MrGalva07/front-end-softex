/*Solicite ao usuário que insira três números decimais. Em seguida, calcule e exiba a média
aritmética desses números. */
let firstDnumber = Number(prompt("Insira o primeiro número decimal"))
let secondDnumber = Number(prompt("Insira o segundo número decimal"))
let thirdDnumber = Number(prompt("Insira o terceiro número decimal"))
let average = (firstDnumber + secondDnumber + thirdDnumber) / 3
alert("A média aritmética dos números é " + average)