/*Receba três números inteiros digitados pelo usuário e exiba o resultado da operação (n1 + n2) *
n3. */
let firstNumero = Number(prompt("Digite um número inteiro"))
let secondNumero = Number(prompt("Digite outro número inteiro"))    
let thirdNumero = Number(prompt("Digite mais um número inteiro"))
let result = (firstNumero + secondNumero) * thirdNumero
